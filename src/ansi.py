# se usa para devolver
# los caracteres del terminal a la normalidad
RESET = "\033[0m"

# estilos de texto
BOLD = "\033[1m"        # negrita
ITALIC = "\033[3m"      # cursiva
UNDERLINE = "\033[4m"   # subrayado
BLINK = "\033[5m"       # parpadeante

# colores de texto
BLACK = "\033[30m"      # negro
RED = "\033[31m"        # rojo
GREEN = "\033[32m"      # verde
YELLOW = "\033[33m"     # amarillo
BLUE = "\033[34m"       # azul
MAGENTA = "\033[35m"    # morado
CYAN = "\033[36m"       # cian
WHITE = "\033[37m"      # blanco

# backgroudns
BG_BLACK = "\033[40m"   # negro
BG_RED = "\033[41m"     # rojo
BG_GREEN = "\033[42m"   # verde
BG_YELLOW = "\033[43m"  # amarillo
BG_BLUE = "\033[44m"    # azul
BG_MAGENTA = "\033[45m" # morado
BG_CYAN = "\033[46m"    # cian
BG_WHITE = "\033[47m"   # blanco
